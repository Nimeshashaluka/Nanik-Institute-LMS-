    private void salaryDetailsLoad() {
        try {
            ResultSet resultSet = com.Mysql.execute("SELECT * FROM `admin_salary` "
                    + "INNER JOIN `admin` ON `admin_salary`.`admin_ad_id`=`admin`.`ad_id` "
                    + "INNER JOIN `monthly` ON `admin_salary`.`monthly_m_id` = `monthly`.`m_id`");

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (resultSet.next()) {
                Vector vector = new Vector();
                vector.add(resultSet.getString("admin_salary_id"));
                vector.add(resultSet.getString("ad_id"));
                vector.add(resultSet.getString("fname"));
                vector.add(resultSet.getString("lname"));
                vector.add(resultSet.getString("mobile"));
                vector.add(resultSet.getString("email"));
                vector.add(resultSet.getString("monthly"));
                vector.add(resultSet.getString("date"));
                vector.add(resultSet.getString("price"));

                dtm.addRow(vector);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void loadMonth() {
        try {
            ResultSet resultSet = com.Mysql.execute("SELECT * FROM `monthly`");

            Vector vector = new Vector();
            vector.add("Select");

            while (resultSet.next()) {
                vector.add(resultSet.getString("monthly"));
                manthMap.put(resultSet.getString("monthly"), resultSet.getInt("m_id"));
            }
            DefaultComboBoxModel model = new DefaultComboBoxModel(vector);
            jComboBox2.setModel(model);

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

