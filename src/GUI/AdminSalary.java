    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String ADId = jTextField1.getText();
        String price = jTextField7.getText();
        Date date = jDateChooser1.getDate();
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd hh:mm:ss");
        String manthId = String.valueOf(jComboBox2.getSelectedItem());

        if (ADId.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Admin ID", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (price.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Admin Salary", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (date == null) {
            JOptionPane.showMessageDialog(this, "Please Select Payment Date", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (manthId.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please Select Admin Payment Manth", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                ResultSet resultSet = com.Mysql.execute("SELECT * FROM `admin_salary` WHERE `admin_ad_id`='" + ADId + "' AND `monthly_m_id`='" + manthMap.get(manthId) + "'");
                if (resultSet.next()) {
                    JOptionPane.showMessageDialog(this, "This Manth already Payment", "Warning", JOptionPane.WARNING_MESSAGE);
                } else {
                    com.Mysql.execute("INSERT INTO `admin_salary` (`price`,`date`,`admin_ad_id`,`monthly_m_id`) "
                            + "VALUES ('" + price + "','" + sdf.format(date) + "','" + ADId + "','" + manthMap.get(manthId) + "')");
                    JOptionPane.showMessageDialog(this, "Payment Sucessful", "Success", JOptionPane.WARNING_MESSAGE);
                    reset();
                }
            } catch (Exception e) {
                e.printStackTrace();
            }

        }
    }//GEN-LAST:event_jButton2ActionPerformed

