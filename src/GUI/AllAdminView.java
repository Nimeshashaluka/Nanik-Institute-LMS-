    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String adId = jTextField2.getText();

        if (adId.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Select Admin Detail", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                ResultSet resultSet = com.Mysql.execute("SELECT * FROM `admin` WHERE `ad_id` ='" + adId + "'");

                if (resultSet.next()) {
                    com.Mysql.execute("DELETE FROM `admin` WHERE `ad_id`='" + adId + "'");
                    reset();
                    loadAdminDetails();
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

