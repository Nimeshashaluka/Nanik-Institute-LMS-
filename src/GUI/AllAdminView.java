    private void loadStatus() {
        try {
            ResultSet resultSet = com.Mysql.execute("SELECT * FROM `status`");

            Vector vector = new Vector();
            vector.add("Select");

            while (resultSet.next()) {
                vector.add(resultSet.getString("status"));
                StatusMap.put(resultSet.getString("status"), resultSet.getInt("st_id"));
            }
            DefaultComboBoxModel model = new DefaultComboBoxModel(vector);
            jComboBox2.setModel(model);
        } catch (Exception e) {
        }
    }

    private void loadGender() {
        try {
            ResultSet resultSet = com.Mysql.execute("SELECT * FROM `gender`");

            Vector vector = new Vector();
            vector.add("Select");

            while (resultSet.next()) {
                vector.add(resultSet.getString("g_name"));
                GenderMap.put(resultSet.getString("g_name"), resultSet.getInt("g_id"));
            }
            DefaultComboBoxModel model = new DefaultComboBoxModel(vector);
            jComboBox1.setModel(model);

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void loadAdminDetails() {
        try {
            ResultSet resultSet = com.Mysql.execute("SELECT * FROM `admin` INNER JOIN `gender` ON `admin`.`gender_g_id` = `gender`.`g_id` INNER JOIN `status` ON `admin`.`status_st_id` = `status`.`st_id`");

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (resultSet.next()) {
                Vector vector = new Vector();
                vector.add(resultSet.getString("ad_id"));
                vector.add(resultSet.getString("fname"));
                vector.add(resultSet.getString("lname"));
                vector.add(resultSet.getString("email"));
                vector.add(resultSet.getString("password"));
                vector.add(resultSet.getString("mobile"));
                vector.add(resultSet.getString("address"));
                vector.add(resultSet.getString("g_name"));
                vector.add(resultSet.getString("status"));

                model.addRow(vector);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

